public class UnaryExample {
    public static void main(String[] args){
        int a = 10;
        boolean isjavafun = true;

        System.out.println(+a);
        System.out.println(-a);

        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);

        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);

        System.out.println(!isjavafun);
    }
}
